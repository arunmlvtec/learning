package com.java.learning.logical;

class GamblingResult {
    private int wins;
    private int totalBets;

    public int getWins() {
        return wins;
    }

    public int getTotalBets() {
        return totalBets;
    }

    public void setWins(int wins) {
        this.wins = wins;
    }

    public void setTotalBets(int totalBets) {
        this.totalBets = totalBets;
    }
}
